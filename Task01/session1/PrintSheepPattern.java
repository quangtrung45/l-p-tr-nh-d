package session1;

public class PrintSheepPattern {
    public static void main(String[] args) {
        System.out.println("          '  '");
        System.out.println("           --");
        System.out.println("          (oo)");
        System.out.println("  /========//");
        System.out.println(" / || @@ ||");
        System.out.println("*  ||----||");
        System.out.println("   vv    vv");
        System.out.println("   ''    ''");
    }
}
